package edu.ntnu.idatt2001.paths.gui;

public class SceneConfig {

    private int width;
    private int height;

    public SceneConfig(int sceneWidth, int sceneHeight) {
        this.width = sceneWidth;
        this.height = sceneHeight;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
